    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

	JFrame ventana = new JFrame();
	ventana.setResizable(true);
	JFileChooser abrir = new JFileChooser();
	abrir.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES );
        abrir.setDialogTitle("Seleccione un directorio o archivo");
        if(rb_eng.isSelected())
            abrir.setDialogTitle("Select a Directory or File");
	abrir.setMultiSelectionEnabled(false);
	FileSystemView vista = FileSystemView.getFileSystemView();
	abrir.setFileSystemView(vista);
	int status = abrir.showDialog(ventana,"Seleccionar Carpeta");
	if (status == JFileChooser.APPROVE_OPTION) {
	    String directorio = abrir.getSelectedFile().getPath();
	    tf_directorio.setText(directorio);
	    master_file = new File(directorio);            
            tf_directorio.setToolTipText(directorio);
	}
    }//GEN-LAST:event_btn_buscarActionPerformed

    
